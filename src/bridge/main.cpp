//
// AirMQ SensorNode — firmware migration bridge (ESP32 + ESP8266)
//
// ESP32:  Phase 1 — detects partition table mismatch, writes new PT + blank
//                   otadata, reboots.
//         Phase 2 — starts AP + bare OTA page to accept real SensorNode fw.
//
// ESP8266: No partition table concept — goes straight to AP + OTA page.
//          (Filesystem is reformatted on first boot by storageInit().)
//
#include <Arduino.h>

#ifdef ESP8266
#  include <ESP8266WiFi.h>
#  include <ESP8266WebServer.h>
   typedef ESP8266WebServer WebServerT;
#  define OTA_BEGIN(sz)  Update.begin(sz)
#  define OTA_ERR()      Update.getErrorString()
#  define RESTART()      ESP.restart()
#else
#  include <WiFi.h>
#  include <WebServer.h>
#  include <Update.h>
#  include "esp_spi_flash.h"
#  include "target_partitions.h"   // generated by scripts/gen_partition_header.py
   typedef WebServer WebServerT;
#  define OTA_BEGIN(sz)  Update.begin(UPDATE_SIZE_UNKNOWN)
#  define OTA_ERR()      Update.errorString()
#  define RESTART()      esp_restart()
#endif

#ifndef BRIDGE_AP_SSID
#  define BRIDGE_AP_SSID "AirMQ-Migrate"
#endif
#ifndef BRIDGE_AP_PASS
#  define BRIDGE_AP_PASS "airmq1234"
#endif

static WebServerT server(80);

// ─── ESP32: partition table migration ────────────────────────────────────────

#ifndef ESP8266

static bool ptMatches() {
    if (target_partitions_bin_len == 0) return false;
    uint8_t* buf = (uint8_t*)malloc(target_partitions_bin_len);
    if (!buf) return false;
    esp_err_t err = spi_flash_read(TARGET_PT_OFFSET, buf, target_partitions_bin_len);
    bool ok = (err == ESP_OK) &&
              (memcmp(buf, target_partitions_bin, target_partitions_bin_len) == 0);
    free(buf);
    return ok;
}

static bool migratePartitionTable() {
    Serial.println("[bridge] Erasing partition table sector...");
    if (spi_flash_erase_range(TARGET_PT_OFFSET, 0x1000) != ESP_OK) {
        Serial.println("[bridge] Erase failed");
        return false;
    }
    Serial.println("[bridge] Writing new partition table...");
    if (spi_flash_write(TARGET_PT_OFFSET,
                        target_partitions_bin,
                        target_partitions_bin_len) != ESP_OK) {
        Serial.println("[bridge] Write failed");
        return false;
    }
    Serial.println("[bridge] Resetting otadata (boot → ota_0)...");
    spi_flash_erase_range(TARGET_OTD_OFFSET, TARGET_OTD_SIZE);
    return true;
}

#endif // !ESP8266

