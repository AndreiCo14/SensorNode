// ─── OTA handlers ─────────────────────────────────────────────────────────────

static bool otaBeginOk = false;

static void handleOtaUpload() {
    HTTPUpload& u = server.upload();
    if (u.status == UPLOAD_FILE_START) {
        otaBeginOk = OTA_BEGIN(u.contentLength);
        Serial.println(otaBeginOk ? "[bridge] OTA begin OK" :
                       String("[bridge] OTA begin failed: ") + OTA_ERR());
    } else if (u.status == UPLOAD_FILE_WRITE) {
        if (otaBeginOk &&
            Update.write(u.buf, u.currentSize) != u.currentSize)
            Serial.println("[bridge] OTA write error");
    } else if (u.status == UPLOAD_FILE_END) {
        if (otaBeginOk) {
            if (Update.end(true)) Serial.println("[bridge] OTA complete");
            else Serial.println(String("[bridge] OTA end failed: ") + OTA_ERR());
        }
    }
}

static void handleOtaResponse() {
    if (!otaBeginOk || Update.hasError()) {
        server.send(500, "text/plain", String("OTA failed: ") + OTA_ERR());
    } else {
        server.send(200, "text/plain", "OK \xe2\x80\x94 rebooting");
        Serial.println("[bridge] OTA done — rebooting");
        delay(500);
        RESTART();
    }
}

// ─── Setup / loop ─────────────────────────────────────────────────────────────

static void startOtaServer() {
    WiFi.softAP(BRIDGE_AP_SSID, BRIDGE_AP_PASS);
    Serial.printf("[bridge] AP \"%s\" @ %s\n",
                  BRIDGE_AP_SSID, WiFi.softAPIP().toString().c_str());
    server.on("/", HTTP_GET, []() {
        server.send_P(200, "text/html", HTML);
    });
    server.on("/ota", HTTP_POST, handleOtaResponse, handleOtaUpload);
    server.onNotFound([]() {
        server.sendHeader("Location", "http://192.168.4.1/", true);
        server.send(302, "text/plain", "");
    });
    server.begin();
    Serial.println("[bridge] HTTP server on port 80");
}

void setup() {
    Serial.begin(115200);
    delay(200);
    Serial.println("\n[bridge] AirMQ migration bridge");

#ifndef ESP8266
    if (!ptMatches()) {
        Serial.println("[bridge] Partition table mismatch — migrating");
        if (migratePartitionTable()) {
            Serial.println("[bridge] Migration OK — rebooting");
            delay(500);
            RESTART();
        } else {
            Serial.println("[bridge] Migration FAILED — halting. Use serial flash.");
            for (;;) delay(1000);
        }
        // not reached
    }
    Serial.println("[bridge] Partition table OK");
#endif

    startOtaServer();
}

void loop() {
    server.handleClient();
}
